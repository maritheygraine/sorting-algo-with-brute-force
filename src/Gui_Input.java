    private void btnSortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortActionPerformed
        if (Gui_Menu.activeInsertion == 1){
            JOptionPane.showMessageDialog(rootPane, "INSERTION");
            this.dispose();
            new Gui_Simulation().setVisible(true);
        }
        else if(Gui_Menu.activeBubble == 1){
            JOptionPane.showMessageDialog(rootPane, "Bubble");
            this.dispose();
            new Gui_Simulation().setVisible(true);
        }
         else if(Gui_Menu.activeHeap == 1){
            JOptionPane.showMessageDialog(rootPane, "Heap");
            this.dispose();
            new Gui_Simulation().setVisible(true);
        }
         else if(Gui_Menu.activeSelection == 1){
            JOptionPane.showMessageDialog(rootPane, "Selection");
            this.dispose();
            new Gui_Simulation().setVisible(true);
        }
    }//GEN-LAST:event_btnSortActionPerformed

