    private void input_tfKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_input_tfKeyPressed
      //  int inputTF = Integer.parseInt(input_tf.getText());
        if(evt.getKeyChar()>='0' && evt.getKeyChar() <= '9'){
           error_msg.setText("");
           input_tf.setEditable(true);
        }
     
        else if (evt.getExtendedKeyCode() == KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode() == KeyEvent.VK_DELETE){
            input_tf.setEditable(true);
            error_msg.setText("");
        }
        else if (evt.getKeyChar() >= 'A' && evt.getKeyChar() >= 'Z'){
            input_tf.setEditable(false);
            error_msg.setText("Invalid input!");
          //  error_msg.setForeground(Color.decode("#9F000F"));
           // error_msg.setFont();
    }
    }//GEN-LAST:event_input_tfKeyPressed

